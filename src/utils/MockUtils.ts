async function loadNewMessagesMock(publicKey: string, lastLoadedMessageTime: Date): Promise<Array<Message>> {
    let result = [];
    for (let i = 0; i <= getRandomIntInclusive(2, 3); i++) {
        let keyPair = keyPairs[getRandomIntInclusive(0, keyPairs.length - 1)];
        lastLoadedMessageTime = new Date(lastLoadedMessageTime.getTime() + 1000 * 60);

        result.push({
            id: crypto.randomUUID(),
            keyFrom: keyPair.publicKey,
            keyTo: publicKey,
            createdAt: JSON.stringify(lastLoadedMessageTime),
            message: await encode(publicKey, keyPair.privateKey, "Hello, I'm " + keyPair.name),
        } as Message);
    }
    return result;
}

function getRandomIntInclusive(min: number, max: number): number {
    min = Math.ceil(min);
    max = Math.floor(max);
    return Math.floor(Math.random() * (max - min + 1) + min);
}

